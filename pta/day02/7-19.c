#include <stdio.h>
/*
7-19 计算天数 (15 分)
本题要求编写程序计算某年某月某日是该年中的第几天。

输入格式:
输入在一行中按照格式“yyyy/mm/dd”（即“年/月/日”）给出日期。
注意：闰年的判别条件是该年年份能被4整除但不能被100整除、或者能被400整除。闰年的2月有29天。

输出格式:
在一行输出日期是该年中的第几天。

输入样例1:
2009/03/02
输出样例1:
61
*/
int main()
{
    int year;//年 
    int month;//月 
    int day;//日 
    scanf("%d/%d/%d",&year,&month,&day);
    
   
     int arr[13] = {0,31,28,31,30,31,30,31,31,30,31,30,31};
    
     if(year % 4 == 0&&year % 100 != 0 || year % 400 == 0 ){//该年年份能被4整除但不能被100整除、或者能被400整除
    	arr[2] = 29;
	}else{
		arr[2] = 28;
	}
	
    int i;
    int sum = 0;
    for(i = 0;i<month;i++){
		sum += arr[i];//将输入的月份所对应的天数累加在sum中 
}
   printf("%d",sum+day);

    return 0;

}
